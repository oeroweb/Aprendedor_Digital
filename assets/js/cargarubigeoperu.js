$(document).ready(function(){
  //console.log("listo");
  $("#cbx_departamentos").change(function(){
    //console.log("cambio");
    $("#cbx_distritos").find('option').remove().end().append("<option value='noubica'></option>").val("noubica");   
    
    $("#cbx_departamentos option:selected").each(function(){
      IdDepartamento = $(this).val();
      //console.log(IdDepartamento);
      $.post("../models/provincias.php", { IdDepartamento }, function(data){
        $("#cbx_provincias").html(data);
      });
    });
  })
});

